package alg;

public class Robot {
    public int startX;
    public int startY;
    public int finishX;
    public int finishY;


    public Robot(int startX, int startY, int finishX, int finishY) {
        this.startX = startX;
        this.startY = startY;
        this.finishX = finishX;
        this.finishY = finishY;
    }


}
